<script>
	
$(function() {

	var item_id = null;
	var add_writing = false;

	var no_writing_button = $("#no-writing-button");
	var yes_writing_button = $("#yes-writing-button");

	var form = $("#order-form");
	var form_writing = $("#order-form-writing");
	var form_item = $("#order-form-item");

	$(".set-item-button").on("click", function() {
		item_id = $(this).data("item-id");
	});

	var make_order = function(ev) {
		ev.preventDefault();
		form_writing.prop("disabled", !add_writing);
		form_item.val(item_id);
		form.submit();
	};

	no_writing_button.on("click", make_order);
	yes_writing_button.on("click", function(ev) {
		add_writing = true;
		make_order(ev);
	});

});

</script>